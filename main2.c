/**
* @brief Function implementing the ReadData thread.
* @param argument: Not used
* @retval None
*/
/* USER CODE END Header_StartReadData */
void StartReadData(void const * argument)
{
  /* USER CODE BEGIN StartReadData */
  /* Infinite loop */
  for(;;)
  {
    osDelay(100);

    //Call all the functions to initialize the needed sensors.
    BSP_HSENSOR_Init();
    humidity = BSP_HSENSOR_ReadHumidity();


    //Magneto XY
    BSP_MAGNETO_Init();
    BSP_MAGNETO_GetXYZ(pDataXYZ);

    //Accelerometer
    BSP_ACCELERO_Init();
    BSP_ACCELERO_AccGetXYZ(pDataXYZaccelerometer);


    //Pressure measure
    BSP_PSENSOR_Init();
    pressure = BSP_PSENSOR_ReadPressure();
  }
  /* USER CODE END StartReadData */
}

